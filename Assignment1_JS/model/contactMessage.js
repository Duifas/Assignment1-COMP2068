const mongoose = require('mongoose')

const ArticleSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true
    },
    description: {
        type: String,
        required: true,
        trim: true
    }
});

const Articles = mongoose.model("Articles", ArticleSchema);
module.exports = Articles; 